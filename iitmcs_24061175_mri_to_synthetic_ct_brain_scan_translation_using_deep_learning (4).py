def train_epoch(model, loader, loss_fn, optimizer):
    model.train()
    epoch_loss = 0
    for mri, ct in loader:
        mri, ct = mri.float().to(device), ct.float().to(device)
        optimizer.zero_grad()
        output = model(mri)
        loss = loss_fn(output, ct)
        loss.backward()
        optimizer.step()
        epoch_loss += loss.item()
    return epoch_loss / len(loader)

